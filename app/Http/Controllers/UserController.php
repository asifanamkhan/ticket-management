    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = auth()->user();
        $countries = [];
        $countryResponse = Country::all();
        if ($countryResponse['status'] === 'success') {
            $countries = $countryResponse['data'];
        }
        return view('visitors.profile', compact('user', 'countries'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $response = User::update($id, [
                'first_name' => $request->first_name,
                'last_name' => $request->last_name,
                'email' => $request->email,
                'gender' => $request->gender,
                'mobile' => $request->mobile,
                'address' => $request->address,
                'city' => $request->city,
                'country_id' => $request->country_id,
            ]);

            if ($response['status'] === 'error') {
                return back()->withInput($request->all())->withErrors($response['data']);
            }

            Session::flash('user-update', 'User updated successfully.');

            return back();
        } catch (\Exception $ex) {
            throw new \Exception($ex->getMessage());
        }
    }

